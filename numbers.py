# decimal calculation
# PEMDAS (or whatever variant of +-*/ you want to call it ^_^)
print(2 + 3)
print(3 - 2)
print(2 * 3)
print(3 / 2)
# exponents (machtsverheffing)
print(3 ** 2)
print(3 ** 3)
print(10 ** 6)
# multiple operators in one expression
print(2 + 3*4)
# using operator precendence here..
print((2 + 3) * 4)
# floating calculation
print(0.1 + 0.1)
print(0.2 * 0.2) # this will return 0.04000000000000001
print(2 * 0.1)
print(2 + 0.2)
# arbitrary number of decimal places
print(0.2 + 0.1)
print(3 * 0.1)
'''The extra decimal places should be ignored. all programming languages have this "problem" which
can be resolved with some measures.'''
